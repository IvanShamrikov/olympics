def foo(a):
    prev = -100000000

    for i in range(N):
        positive = abs(a[i])
        negative = -abs(a[i])

        if prev <= negative:
            print("--> 1")
            a[i] = negative
            prev = a[i]
            continue
        elif prev <= positive:
            print("--> 2")
            a[i] = positive
            prev = a[i]
            continue
        else:
            return False
    return True

if foo(a):
    print("YES")
    print(a)
else:
    print("NO")

